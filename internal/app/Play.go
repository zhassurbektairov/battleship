package app

import (
	. "battleship/internal/config"
	utils "battleship/internal/utils"
	"fmt"
)

func Play(user *Data) {
	var input string
	fmt.Scanln(&input)

	x := int(input[0] - 'A')
	y := int(input[1]-'0') - 1

	if user.InitialMap[x][y] == 'V' {
		user.ShowMap[x][y] = 'x'

		id := user.ShipID[x][y]
		user.ShipHealth[id]--

		if user.ShipHealth[id] == 0 {
			utils.RevealNeighbours(user, x, y)
			user.ShipCount--
		}

		if user.ShipCount == 0 {
			State = false
		}
	} else if user.InitialMap[x][y] == '.' {
		user.ShowMap[x][y] = '-'
	}

	utils.PrintMap(user)
}

